        else
        {
            ligado = false;
            inicio.enviaDados(2);
            inicio.enviaDados(4);
            inicio.enviaDados(6);
            inicio.enviaDados(8);
            inicio.enviaDados(10);
            inicio.enviaDados(12);
            jButtononoff.setText("Ligado, Clique para desligar tudo");
            jButtononoff.setForeground(Color.red);
            try
            {
            Thread.sleep(200);
            }
            catch(Exception erro)
            {
                
            }
           jButtononoff.setText("Ligar todas as luzes");
           jButtononoff.setForeground(Color.BLACK);
            
            
        }
        
        
