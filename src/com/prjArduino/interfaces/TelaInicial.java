    private void jMenuItemAlternarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemAlternarActionPerformed
        // TODO add your handling code here:
        // apagando todas as luzes antes de começar o ciclo
        inicio.enviaDados(8);
        inicio.enviaDados(10);
        inicio.enviaDados(12);
        int cont = 0;
        if(piscinaalternado==false){
           while(cont<10){
            try {
                    
                    piscinaazul=true;
                    inicio.enviaDados(7);
                    Thread.sleep(1000);
                    piscinaazul=false;
                    inicio.enviaDados(8);
                    piscinaverde=true;
                    inicio.enviaDados(11);
                    Thread.sleep(1000);
                    piscinaverde=false;
                    inicio.enviaDados(12);
                    piscinavermelha=true;
                    inicio.enviaDados(9);
                    Thread.sleep(1000);
                    piscinavermelha=false;
                    inicio.enviaDados(10);
                    cont++;
                } 
                catch (InterruptedException e) {
                    break;
                }
           }
           
        }
    }//GEN-LAST:event_jMenuItemAlternarActionPerformed

